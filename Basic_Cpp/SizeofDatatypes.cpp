# include<bits/stdc++.h>
using namespace std;

int32_t main()
{
    int a; // declaration;
    a = 10;

    cout << "Size of int : " << sizeof(a) << endl;

    float b;

    cout << "Size of float : " << sizeof(b) << endl;

    char c;

    cout << "Size of char : " << sizeof(c) << endl;

    bool d;

    cout << "Size of bool : " << sizeof(d) << endl;
    
    short int si;
    long int li;

    cout << "Size of short_int : " << sizeof(si) << endl;

    cout << "Size of long_int : " << sizeof(li) << endl;

    return 0;
}